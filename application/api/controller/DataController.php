    public function back($id)
    {
        $M = Data::get($id);

        if ($M->getData('speed') == 1) {
            $M->speed = -1;
            $M->status = Data::COMMIT;
            $M->I_data = null;
        } else if ($M->getData('speed') == 2) {
            $M->speed = 1;
            $M->status = Data::SIGN;
            $M->II_data = null;
        } else if ($M->getData('speed') == 3) {
            //应届生
            if ($M->getData('mode') == '03') {
                $M->speed = -1;
                $M->status = Data::COMMIT;
                $M->I_data = null;
            }
            $M->speed = 2;
            $M->status = Data::SUBMIT;
        }

        return $M->save() ? Res::Json(200) : Res::Json(400);
    }

