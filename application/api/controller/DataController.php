     * 一审
     * @param $id
     * @return \think\response\Json
     */
    public function sign($id)
    {
        $M = Data::get($id);

        //不是未办状态
        if ($M->getData('speed') != -1)
            return Res::Json(400, '当前办理进度信息不符..核实当前进度!!');

        //应届生直接完成
        if ($M->getData('mode') == '03') {
            $M->speed = 3;
            $M->status = Data::GET_DIAOLING;
        } else {
            //一审
            $M->speed = 1;
            $M->status = Data::SIGN;
        }

        if ($M->I_date == null)
            $M->I_date = time();

        return $M->save() ? Res::Json(200) : Res::Json(400);
    }

