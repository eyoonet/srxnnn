    /**
     * 方法返回用户分组规则SQL格式查询条件
     * @param $group_id
     * @return array|bool
     */
    private function groupWhere($group_id)
    {
        $uid = session('uid');
        switch ($group_id) {
            case 1: //管理员
                $where = true;
                break;
            case 2: //业务员
                $where = ['user_id' => $uid];
                break;
            case 3: //内勤
                $where = "`data`.`nuser_id`=$uid OR `data`.`user_id`=$uid  OR `data`.`status`=" . Data::NEW_CLIENT;
                break;
            case 4: //外勤
                $where = "`data`.`wuser_id`=$uid OR `data`.`user_id`=$uid";
                break;
        }
        return $where;
    }

    /**
     * 菜单获取表格格式数据
