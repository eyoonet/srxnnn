    /**
     * 一审
     * @return bool|false|int
     */
    public function sign()
    {
        if ($this->getData('id')) {
            //不是未办状态
            if ($this->getData('speed') != -1) {
                $this->error = '当前办理进度信息不符..核实当前进度!';
                return false;
            }
            //应届生直接完成
            if ($this->getData('mode') == '03') {
                $this->speed = 3;
                $this->status = self::GET_DIAOLING;
            } else {
                //一审
                $this->speed = 1;
                $this->status = self::SIGN;
            }
            if ($this->I_date == null)
                $this->I_date = time();
            $this->rcdate = null;
            return $this->save() ? true : false;
        } else {
            $this->error = '空数据';
            return false;
        }
    }

